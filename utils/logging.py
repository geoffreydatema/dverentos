def debug(string):
    print(f"[DEBUG] {string}")

def ddebug(string):
    print(f"--------------------------------\n[DEBUG] {string}\n--------------------------------")

def info(string):
    print(f"[INFO] {string}")

def warning(string):
    print(f"[WARNING] {string}")

def error(string):
    print(f"[ERROR] {string}")

def syntax_error(string):
    print(f"[SYNTAX ERROR] {string}")
